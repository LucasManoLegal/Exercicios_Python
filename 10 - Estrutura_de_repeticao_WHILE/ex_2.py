# 2. Faça uma contagem regressiva de 10 até 1 e exiba "Fim!".

num = 10 # a contagem é regressiva, começando do 10.

while num >= 1: # a lógica é a mesma do anterior, enquanto o número for MAIOR ou IGUAL a 1
    print(num) # Imprimir na tela o número atual
    num -= 1 # Retira 1 da contagem
print("Fim!")